#include <bits/stdc++.h>

using namespace std;

void solve(){
	int n,m; cin >> n >> m;
	vector<int> cakes(n);
	for(int i=0; i < n; i++){
		int temp; cin >> temp;
		cakes[i] = temp;
	}

}

int main(){
	ios_base::sync_with_stdio(0);
	cin.tie(0);
	int t; cin >> t;
	while(t--){
	solve();
	}
	return 0;
}
